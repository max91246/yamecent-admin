            $types = explode('/', preg_replace('/\(.*?\)/', '', $typeStr));
            $type1 = trim($types[0]);
            $type2 = isset($types[1]) ? trim($types[1]) : null;
            if ($type2 === '') $type2 = null;

            $weakness = array_values(array_filter(array_map(
                fn($w) => trim(preg_replace('/\(.*?\)/', '', $w)),
                explode('、', $weakStr)
            ), fn($w) => $w !== ''));

            $moveType    = $type1;
            $isGigantamax = (int)(str_contains($name,'極巨') || str_contains($name,'超極巨'));
            $isMega       = (int)(str_contains($name,'超級'));

            $rows[] = [
                'card_no'      => $cardNo,
                'series'       => $series,
                'name'         => $name,
                'type1'        => $type1,
                'type2'        => $type2,
                'move_type'    => $moveType,
                'weakness'     => json_encode($weakness, JSON_UNESCAPED_UNICODE),
                'grade'        => $grade,
                'is_gigantamax'=> $isGigantamax,
                'is_mega'      => $isMega,
                'image_url'    => null,
                'created_at'   => $now,
                'updated_at'   => $now,
            ];
        }

        DB::table('mezastar_pokemons')->insert($rows);

        // ── 3. 清除 Pokemon 快取 ────────────────────────────────────
        Cache::forget('mezastar_pokemon:all');
        Cache::forget('mezastar_pokemon:all_list');

        $total     = count($rows);
        $gigantamax = collect($rows)->where('is_gigantamax', 1)->count();
        $mega       = collect($rows)->where('is_mega', 1)->count();

        $this->command->info("✅ 新資料寫入完成：共 {$total} 張，極巨化 {$gigantamax} 張，超級進化 {$mega} 張");
        $this->command->info("📌 提醒：請接著執行 php artisan scrape:mezastar 更新圖片");
    }
